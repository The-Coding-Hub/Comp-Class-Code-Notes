# Looping through a String!

a = 'INDIA'
for i in a:
	print(i)

# Looping through a List!

b = [10, 'INDIA', 78.2]
for j in b:
	print(j)

# Looping through a range()!

c = range(1, 11)
for k in c:
	print(k)

# For Loop with Else Block
d = [1, 2, 3, 4, 5, 6]
for l in d:
	print(l)
else:
	print("Thank You")